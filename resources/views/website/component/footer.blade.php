<script>
    $('document').ready(function() {
        $('#toast').hide();
        $('#subBtn').on('click', function(e) {
            e.preventDefault();
            var formData = $('#contactForm').serializeArray();
            console.log(formData);
            $.ajax({
                type: 'POST',
                url: 'send_mail.php',
                data: formData,
                success: function(response) {
                    $('#toast').show();
                    $('#contactForm')[0].reset();
                    setTimeout(function() {
                        $('#toast').hide();
                    }, 3000);
                }
            });
        })
    });

    function closeToast() {
        $('#toast').hide();
    }
</script>