<script>
    $('document').ready(function () {
       $('#snacktoast').hide();
        $('#subBtns').on('click', function (e) {
            e.preventDefault();

            var formData = $('#popupForm').serializeArray();
            console.log(formData);

            $.ajaxSetup({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                }
            });

            var  name = $('#first_name').val();
            var  email_address = $('#email_address').val();
            var  phone = $('#phone').val();
            var  message = $('#message').val();
            if (name == ''  && email_address == ''  && phone == ''  && message == '' ) {
                $('#snacktoast').show();
                $('#snacktoast').text('Please enter all fields!');
                setTimeout(function () {
                    $('#snacktoast').hide();
                }, 2000);
                return false;
            }else{
                $.ajax({
                    type: 'POST',
                    url: "{{ url('/popup') }}",
                    data: formData,
                    success: function (response) {
                        $('#snacktoast').show();
                        $('#snacktoast').text('Your message has been sent successfully!');
                        $('#popupForm')[0].reset();
                        setTimeout(function () {
                            $('#snacktoast').hide();
                        }, 3000);
                    }
                });
            }
        });
    });
</script>
