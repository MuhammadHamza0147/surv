    <script>
        $('document').ready(function () {
            $('#snackbar').hide();
            $('#subBtn').on('click', function (e) {
                e.preventDefault();
                if (!validateForm()) {
                    return;
                }
                
                var formData = $('#contactForm').serializeArray();
                console.log(formData);
    
                $.ajaxSetup({
                    headers: {
                        'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                    }
                });
    
                $.ajax({
                    type: 'POST',
                    url: "{{ route('contactus') }}",
                    data: formData,
                    success: function (response) {
                        $('#snackbar').show();
                        $('#snackbar').text('Your message has been sent successfully!');
                        $('#contactForm')[0].reset();
                        grecaptcha.reset();
                        setTimeout(function () {
                            $('#snackbar').hide();
                        }, 3000);
                    }
                });
            });
        });
        
        function validateForm() {
            var isValid = true;
    
            $('#contactForm input, #contactForm textarea[name="message"]').each(function () {
                if ($(this).val().trim() === '') {
                    isValid = false;
                    return false;
                }
            });
    
            if (!isValid) {
                $('#snackbar').show();
                $('#snackbar').text('Please enter all fields!');
                setTimeout(function () {
                    $('#snackbar').hide();
                }, 2000);
                return false;
            }
            
            return true;
        }
    
        function closeToast() {
            $('#snackbar').hide();
        }
    </script>
